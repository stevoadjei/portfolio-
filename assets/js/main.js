document.addEventListener('DOMContentLoaded', function () {
  const slides = document.querySelectorAll('.hero-slide');
  let currentSlide = 0;

  // Swiper initialization
  const swiper = new Swiper('.swiper', {
    loop: true,
    autoplay: {
      delay: 5000, // Autoplay delay for 5 seconds (customize as needed)
      disableOnInteraction: false
    },
    pagination: {
      el: '.swiper-pagination',
      clickable: true, // Allows clicking on the pagination bullets to navigate
    },
    breakpoints: {
      640: {
        slidesPerView: 1,
        spaceBetween: 10
      },
      768: {
        slidesPerView: 1,
        spaceBetween: 20
      },
      1024: {
        slidesPerView: 1,
        spaceBetween: 30
      }
    }
  });

  // Retain your sliding functionality
  function nextSlide() {
    slides[currentSlide].classList.remove('active');
    currentSlide = (currentSlide + 1) % slides.length;
    slides[currentSlide].classList.add('active');
  }

  // Initially show the first slide
  slides[currentSlide].classList.add('active');

  // Auto slide every 0.9 seconds (900ms)
  setInterval(nextSlide, 900);
});

//slider ends here

